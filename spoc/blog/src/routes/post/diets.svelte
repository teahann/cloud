      <h3>Intro</h3>
      <p>
        Diets are niche desires for metabolic optimizations. Instead of the hyping up benefits and selling you something approach, I'll break down main appeals of specific diets that I consider worthwhile.
      </p>
    </section>
    <section>
      <h3>Anti-inflammatory</h3>
      <p>
        Inflammation can be summarized as cellular stress. Metabolically, this happens when consuming unnatural and toxic compounds. The most common being purified compounds, which is common in oils, refined wheat, grain-fed meat, and purified carbohydrates.
      </p>
      <p>
          If unable to avoid inflammatory compounds, increasing omega-3 and antioxidants should accelerate the immune response.
      </p>
    </section>
    <section>
      <h3>Intermittent fasting</h3>
      <p>
        Not eating for an extended period of time activates certain processes. After 12-22 hours of no carbohydrates, glycogen stores are depleted. This shifts the body into ketosis, activates autophagy, regenerates immune cells and improves cardiovascular function. Doing this intermittently provides long-term benefits and energy stability.
      </p>
    </section>
    <section>
      <h3>Ketogenic</h3>
      <p>
        This focuses on minimizing carbohydrates, just enough amino acids, and primarily consuming lipids. The idea is to minimize time spent getting energy from glycogen. Pairs with some form of intermittent fasting.
      </p>
    </section>
