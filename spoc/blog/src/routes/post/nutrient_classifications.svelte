      <h3>Intro</h3>
      <p>
        Given the required dependency of external systems to provide metabolic compounds, we have created abstractions for metabolic reasoning. Edible compounds are considered nutrients, this is a useful abstraction.
        However some abstractions are outdated. Let's update what some understandings.
      </p>
    </section>
    <section>
      <h3>Protein</h3>
      <p>
        This might be the most broad description in nutrition. With over 100,000 unique proteins in the human body alone, tracking each type of protein wouldn't be practical. Tracking amino acid quantities is practical, and provides deeper metabolic insight.
      </p>
    </section>
    <section>
      <h3>Sugar</h3>
      <p>
        In the past century, we have come to understand that sugars are a simple carbohydrate. Predictions gain more insight while considering it as such.
      </p>
    </section>
    <section>
      <h3>Macros and calories</h3>
      <p>These are low value metrics. It makes sense as on of the first iterations into "improving health," but it's an entry level metabolic abstraction.</p>
